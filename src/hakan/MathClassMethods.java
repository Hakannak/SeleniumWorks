package hakan;

public class MathClassMethods {

	public static void main(String[] args) {
		System.out.println(Math.abs(-26)); //26
		System.out.println(Math.pow(2, 4)); //16
		System.out.println(Math.ceil(8.2)); //9.0
		System.out.println(Math.floor(5.7)); //5.0
		System.out.println(Math.max(15,12)); //15
		System.out.println(Math.min(11, 22)); //11
		System.out.println(Math.sqrt(16)); //4
		

	}

}
